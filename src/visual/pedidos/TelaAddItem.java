    private void btnProcurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcurarActionPerformed
        if(validaConsulta()){
            produto = produtoDao.consulta(txtConsulta.getText());
            if(produto!=null){
                txtProduto.setText(produto.getNome());
                txtValor.setText(String.valueOf(produto.getValor()));
                btnAdicionar.setEnabled(true);
                txtQuantidade.requestFocus();
            }else{
                txtConsulta.requestFocus();
                JOptionPane.showMessageDialog(null, "Erro: Codigo invalido");
            }
        }
    }//GEN-LAST:event_btnProcurarActionPerformed

    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        if(validaCampo()){
            int quantidade = Integer.parseInt(txtQuantidade.getText());
            if(quantidade<=produto.getQuantidade()){
                PedidoTemProduto pedidoTemProduto = new PedidoTemProduto();
                pedidoTemProduto.setCodigoPedido(codigoPedido);
                pedidoTemProduto.setCodigoProduto(produto.getCodigo());
                pedidoTemProduto.setProduto(produto.getNome());
                pedidoTemProduto.setValor(produto.getValor()*quantidade);
                pedidoTemProduto.setQuantidadeEstoque(produto.getQuantidade()-quantidade);
                pedidoTemProduto.setQuantidade(quantidade);
                pedido.adicionaLinha(pedidoTemProduto);
                
                if(telaAltPed!=null){
                    telaAltPed.setValorTotal(pedido.getValorTotal());
                }else if(telaNewPed!=null){
                    telaNewPed.setValorTotal(pedido.getValorTotal());
                }
                
                dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Erro: Quantidade excede o valor em estoque");
            }
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

    
