    private void btnCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastroActionPerformed
        Funcionario funcionario = new Funcionario();

        if (validaCampos()) {
            funcionario.setRa(txtRa.getText());
            funcionario.setNome(txtNome.getText());
            funcionario.setCargo(txtCargo.getText());
            funcionario.setSalario(Double.parseDouble(txtSalario.getText()));
            funcionario.setDataNacimento(txtNascimento.getText());
            funcionario.setLagradouro(txtLagradouro.getText());
            funcionario.setNumero(Integer.parseInt(txtNumero.getText()));
            funcionario.setBairro(txtBairro.getText());
            funcionario.setCep(txtCep.getText());
            funcionario.setEstado(cbxEstado.getSelectedItem().toString());
            funcionario.setCidade(txtCidade.getText());
            funcionario.setEstadoCivil(cbxEstadoCivil.getSelectedItem().toString());
            switch (funcionarioDao.salvar(funcionario)) {
                case 1: {
                    JOptionPane.showMessageDialog(null, "Salvo com sucesso");
                    dispose();
                    break;
                }case 5: {
                    JOptionPane.showMessageDialog(null, "Erro: Bando de dados travado");
                    dispose();
                    break;
                }
                default: {
                    JOptionPane.showMessageDialog(null, "Erro ao cadastrar");
                    break;
                }
            }
        }
    }//GEN-LAST:event_btnCadastroActionPerformed

    private boolean validaCampos() {
        boolean status = true;
        if (txtRa.getText().length()<1) {
            JOptionPane.showMessageDialog(null, "Erro: RA invalido");
            txtRa.requestFocus();
            status = false;
        }
        else if (txtNome.getText().length()<2) {
            JOptionPane.showMessageDialog(null, "Erro: Nome invalido");
            txtNome.requestFocus();
            status = false;
        }
        else if (txtNascimento.getText().equals("  /  /    ")) {
            JOptionPane.showMessageDialog(null, "Erro: Nascimento invalido");
            txtNascimento.requestFocus();
            status = false;
        }
        else if (cbxEstadoCivil.getSelectedItem().toString().length()==1) {
            JOptionPane.showMessageDialog(null, "Erro: Selecione um estado civil");
            cbxEstadoCivil.requestFocus();
            return false;
        }
        else if (txtCargo.getText().length()<2) {
            JOptionPane.showMessageDialog(null, "Erro: Cargo invalido");
            txtCargo.requestFocus();
            status = false;
        }
        else if (txtSalario.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Erro: Salario invalido");
            txtSalario.requestFocus();
            status = false;
        }
        else if (txtLagradouro.getText().length()<2) {
            JOptionPane.showMessageDialog(null, "Erro: Logradouro invalido");
            txtLagradouro.requestFocus();
            status = false;
        }
        else if (txtCep.getText().equals("     -   ")) {
            JOptionPane.showMessageDialog(null, "Erro: Cep invalido");
            txtCep.requestFocus();
            status = false;
        }
        else if (txtBairro.getText().length()<2) {
            JOptionPane.showMessageDialog(null, "Erro: Bairro invalido");
            txtBairro.requestFocus();
            status = false;
        }
        else if (txtCidade.getText().length()<2) {
            JOptionPane.showMessageDialog(null, "Erro: Cidade invalido");
            txtCidade.requestFocus();
            status = false;
        }else if (cbxEstado.getSelectedItem().toString().length()==1) {
            JOptionPane.showMessageDialog(null, "Erro: Selecione um estado");
            cbxEstado.requestFocus();
            return false;
        }else{
            int numero;
            try {
                numero = Integer.parseInt(txtNumero.getText());
            } catch (NumberFormatException e) {
                numero=0;
            }
            if (numero<=0||numero>20000) {
                JOptionPane.showMessageDialog(null, "Erro: Numero invalido");
                txtNumero.requestFocus();
                return false;
            }
        }

        return status;
    }

