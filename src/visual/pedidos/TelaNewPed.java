    private void alterarEstoque(List<PedidoTemProduto> produtosDoPedido){
        produtosDoPedido.forEach(produtoDoPedido -> {
            ProdutoDao estoque = new ProdutoDao();
            estoque.updadeEstoque(produtoDoPedido.getCodigoProduto(), produtoDoPedido.getQuantidadeEstoque()-produtoDoPedido.getQuantidade());
        });
    }
    
