    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        ClienteDao clienteDao = new ClienteDao();
        if (validaConsulta()) {
            String consulta = txtCpfCnpj.getText();
            if (consulta.length() == 11) {
                Fisico cliente = clienteDao.consultaFisico(consulta);
                if (cliente != null) {
                    txtNome.setText(cliente.getNome());
                    codigoCliente = cliente.getCodigoCliente();
                } else {
                    JOptionPane.showMessageDialog(null, "Erro: Cliente não encontrado");
                    txtCpfCnpj.requestFocus();
                }
            } else {
                Juridico cliente = clienteDao.consultaJuridico(consulta);
                if (cliente != null) {
                    txtNome.setText(cliente.getNome());
                    codigoCliente = Integer.parseInt(consulta);
                } else {
                    JOptionPane.showMessageDialog(null, "Erro: Cliente não encontrado");
                    txtCpfCnpj.requestFocus();
                }
            }
        }
        clienteDao.desconectar();
    }//GEN-LAST:event_btnConsultarActionPerformed

