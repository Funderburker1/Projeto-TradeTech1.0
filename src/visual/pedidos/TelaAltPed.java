    private void alterarEstoque(List<PedidoTemProduto> produtosDoPedido){
        
        listaProdutosPedidoNaoAlterado.forEach(produtoPedidoNaoAlterado -> {
            boolean status = false;
            for (int i = 0; i < produtosDoPedido.size(); i++) {
                if(produtosDoPedido.get(i).getCodigoProduto()==produtoPedidoNaoAlterado.getCodigoProduto()){
                    status=true;
                }
            }
            if(!status){
                ProdutoDao estoque = new ProdutoDao();
                estoque.updadeEstoque(produtoPedidoNaoAlterado.getCodigoProduto(), produtoPedidoNaoAlterado.getQuantidadeEstoque()+produtoPedidoNaoAlterado.getQuantidade());
                estoque.desconectar();
            }
        });
        
        produtosDoPedido.forEach(produtoPedido->{
            ProdutoDao estoque = new ProdutoDao();
            estoque.updadeEstoque(produtoPedido.getCodigoProduto(), produtoPedido.getQuantidadeEstoque());
        });
    }
    
