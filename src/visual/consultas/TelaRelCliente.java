    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        switch (validaConsulta()) {
            case 1:
                
                try {
                    txtCpfCnpj.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(new javax.swing.text.MaskFormatter("###.###.###-##")));
                } catch (java.text.ParseException ex) {
                    ex.printStackTrace();
                }
                
                btnCpf.setSelected(true);
                btnCnpj.setSelected(false);
                txtDataNascimento.setEnabled(true);
                cbxEstadoCivil.setEnabled(true);
                Fisico cliente;
                
                cliente = clienteDao.consultaFisico(txtConsulta.getText());
                
                if(cliente!=null){
                    txtCpfCnpj.setText(cliente.getCpf());
                    txtNome.setText(cliente.getNome());
                    txtDataNascimento.setText(cliente.getDataNascimento());
                    cbxEstadoCivil.setSelectedItem(cliente.getEstadoCivil());
                    txtLogradouro.setText(cliente.getLagradouro());
                    txtNumero.setText(String.valueOf(cliente.getNumero()));
                    txtCep.setText(cliente.getCep());
                    txtBairro.setText(cliente.getBairro());
                    txtCidade.setText(cliente.getCidade());
                    cbxEstado.setSelectedItem(cliente.getEstado());
                    btnEditar.setEnabled(true);
                    clienteFisicoConsultado = cliente;
                }else{
                    JOptionPane.showMessageDialog(null, "Erro: CPF não cadastrado");
                }
                
                break;
            case 2:
                
                try {
                    txtCpfCnpj.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(new javax.swing.text.MaskFormatter("##.###.###/####-##")));
                } catch (java.text.ParseException ex) {
                    ex.printStackTrace();
                }
                
                btnCpf.setSelected(false);
                btnCnpj.setSelected(true);
                txtDataNascimento.setEnabled(false);
                cbxEstadoCivil.setEnabled(false);
                Juridico clienteJuridico;
                clienteJuridico = clienteDao.consultaJuridico(txtConsulta.getText());
                
                if(clienteJuridico!=null){
                    txtCpfCnpj.setText(clienteJuridico.getCnpj());
                    txtNome.setText(clienteJuridico.getNome());
                    txtLogradouro.setText(clienteJuridico.getLagradouro());
                    txtNumero.setText(String.valueOf(clienteJuridico.getNumero()));
                    txtCep.setText(clienteJuridico.getCep());
                    txtBairro.setText(clienteJuridico.getBairro());
                    txtCidade.setText(clienteJuridico.getCidade());
                    cbxEstado.setSelectedItem(clienteJuridico.getEstado());
                    btnEditar.setEnabled(true);
                    clienteJuridicoConsultado = clienteJuridico;
                }else{
                    JOptionPane.showMessageDialog(null, "Erro: CNPJ não cadastrado");
                }
                break;
            default:
                JOptionPane.showMessageDialog(null, "Erro: CPF/CNPJ invalido");
                break;
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

