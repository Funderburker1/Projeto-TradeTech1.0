    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if(validaCampos()){
            if(txtSenha.getText().equals(txtConfirmaSenha.getText())){
                Login login = new Login();
                LoginDao loginDao = new LoginDao();

                login.setUsuario(txtUsuario.getText());
                login.setSenha(txtSenha.getText());
                login.setRa(txtRa.getText());

                switch(loginDao.salvar(login)){
                    case 1:{
                        JOptionPane.showMessageDialog(null,"Usuario cadastrado");
                        dispose();
                        break;
                    }
                    default:{
                        JOptionPane.showMessageDialog(null,"Erro ao Cadastrar");
                        break;
                    }
                }
            }
        }else{
            JOptionPane.showMessageDialog(null,"Erro: algum campo errado");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

