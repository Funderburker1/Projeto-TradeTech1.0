    private boolean validaCampo(){
        try {
            int n = Integer.parseInt(txtQuantidade.getText());
            if (n>0){
                return true;
            }
        } catch (NumberFormatException e) {
            txtQuantidade.requestFocus();
        }
        JOptionPane.showMessageDialog(null, "Erro: Quantidade invalida");
        return false;
    }
    
    private boolean validaConsulta(){
        if(txtConsulta.getText().length()>=1){
            return true;
        }
        txtConsulta.requestFocus();
        JOptionPane.showMessageDialog(null, "Erro: Codigo invalido");
        return false;
    }
    
    public void atualizaTabela(){
        tableModel.limpaTabela();
        tableModel.adicionaVariasLinhas(produtoDao.consultaTodos());
    }
    
