    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        if(validaCampos()){
            Pedido pedido = new Pedido();
            pedido.setCodigo(codigoPedido);
            pedido.setTelefone(txtTelefone.getText());
            pedido.setCep(txtCep.getText());
            pedido.setBairro(txtBairro.getText());
            pedido.setEntrega(entrega);
            pedido.setLogradouro(txtEndereco.getText());
            pedido.setCidade(txtCidade.getText());
            pedido.setNumero(Integer.parseInt(txtNumero.getText()));
            pedido.setCodigoCliente(codigoCliente);
            pedido.setValor(tableModel.getValorTotal());
            if(pedidoDao.salvar(pedido)==1){
                alterarEstoque(pedidoTemProdutoDados);
                if(pedidoTemProdutoDao.salvar(pedidoTemProdutoDados)!=1){
                    JOptionPane.showMessageDialog(null, "Erro ao salvar pedido");
                    pedidoTemProdutoDao.excluir(codigoPedido);
                    pedidoDao.excluir(codigoPedido);
                }else{
                    JOptionPane.showMessageDialog(null, "Pedido realizado com sucesso");
                    dispose();
                }
            }else{
                JOptionPane.showMessageDialog(null, "Erro ao salvar pedido");
            }
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

