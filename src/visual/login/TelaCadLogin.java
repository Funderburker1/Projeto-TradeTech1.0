    private boolean validaCampos(){
        boolean status=true;
        
        if(txtRa.getText().length()<1){
            JOptionPane.showMessageDialog(null,"Erro: RA invalido");
            txtRa.requestFocus();
            status= false;
        }
        else if(txtUsuario.getText().length()<1){
            JOptionPane.showMessageDialog(null,"Erro: Usuario invalido");
            txtUsuario.requestFocus();
            status= false;
        }
        else if(txtSenha.getText().length()<1){
            JOptionPane.showMessageDialog(null,"Erro: Sanha invalida");
            txtSenha.requestFocus();
            status= false;
        }
        else if(txtConfirmaSenha.getText().length()<1){
            JOptionPane.showMessageDialog(null,"Erro: Confirmação de senha invalida");
            txtConfirmaSenha.requestFocus();
            status= false;
        }
        return status;
    }
    
