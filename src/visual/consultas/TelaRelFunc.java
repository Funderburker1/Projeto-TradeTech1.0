    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if(validaCampos()){
            Funcionario funcionario = new Funcionario();
            
            funcionario.setRa(txtRa.getText());
            funcionario.setNome(txtNome.getText());
            funcionario.setCargo(txtCargo.getText());
            funcionario.setSalario(1000.00);
            funcionario.setDataNacimento(txtDataNascimento.getText());
            funcionario.setLagradouro(txtLagradouro.getText());
            funcionario.setNumero(Integer.parseInt(txtNumero.getText()));
            funcionario.setBairro(txtBairro.getText());
            funcionario.setCep(txtCep.getText());
            funcionario.setEstado(cbxEstado.getSelectedItem().toString());
            funcionario.setCidade(txtCidade.getText());
            funcionario.setEstadoCivil(cbxEstadoCivil.getSelectedItem().toString());
            
            switch (funcionarioDao.updade(funcionario)) {
                case 1: {
                    JOptionPane.showMessageDialog(null, "Salvo com sucesso");
                    dispose();
                    break;
                }
                default: {
                    JOptionPane.showMessageDialog(null, "Erro ao alterar");
                    break;
                }
            }
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        Funcionario funcionario;
        
        if (validaConsultar()) {
            funcionario = funcionarioDao.consulta(txtConsultaRa.getText());
            if(funcionario!=null){
                txtRa.setText(funcionario.getRa());
                txtSalario.setText(String.valueOf(funcionario.getSalario()));
                txtCargo.setText(funcionario.getCargo());
                txtNome.setText(funcionario.getNome());
                txtDataNascimento.setText(funcionario.getDataNacimento());
                cbxEstadoCivil.setSelectedItem(funcionario.getEstadoCivil());
                txtLagradouro.setText(funcionario.getLagradouro());
                txtNumero.setText(String.valueOf(funcionario.getNumero()));
                txtCep.setText(funcionario.getCep());
                txtBairro.setText(funcionario.getBairro());
                txtCidade.setText(funcionario.getCidade());
                cbxEstado.setSelectedItem(funcionario.getEstado());
                btnEditar.setEnabled(true);
            }else{
                JOptionPane.showMessageDialog(null, "Erro: Funcionario n√£o encontrado");
                btnEditar.setEnabled(false);
            }
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

    private boolean validaConsultar(){
        boolean status = true;
        if (txtConsultaRa.getText().length()<1) {
            JOptionPane.showMessageDialog(null, "Erro: RA invalido");
            txtConsultaRa.requestFocus();
            status = false;
        }
        return status;
    }
    
    private boolean validaCampos() {
        boolean status = true;
        
        if (txtNome.getText().length()<2) {
            JOptionPane.showMessageDialog(null, "Erro: Nome invalido");
            txtNome.requestFocus();
            status = false;
        }
        else if (txtDataNascimento.getText().equals("  /  /    ")) {
            JOptionPane.showMessageDialog(null, "Erro: Nascimento invalido");
            txtDataNascimento.requestFocus();
            status = false;
        }
        else if (cbxEstadoCivil.getSelectedItem().toString().length()==1) {
            JOptionPane.showMessageDialog(null, "Erro: Selecione um estado civil");
            cbxEstadoCivil.requestFocus();
            return false;
        }
        else if (txtCargo.getText().length()<2) {
            JOptionPane.showMessageDialog(null, "Erro: Cargo invalido");
            txtCargo.requestFocus();
            status = false;
        }
        else if (txtSalario.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Erro: Salario invalido");
            txtSalario.requestFocus();
            status = false;
        }
        else if (txtLagradouro.getText().length()<2) {
            JOptionPane.showMessageDialog(null, "Erro: Logradouro invalido");
            txtLagradouro.requestFocus();
            status = false;
        }
        else if (txtCep.getText().equals("     -   ")) {
            JOptionPane.showMessageDialog(null, "Erro: Cep invalido");
            txtCep.requestFocus();
            status = false;
        }
        else if (txtBairro.getText().length()<2) {
            JOptionPane.showMessageDialog(null, "Erro: Bairro invalido");
            txtBairro.requestFocus();
            status = false;
        }
        else if (txtCidade.getText().length()<2) {
            JOptionPane.showMessageDialog(null, "Erro: Cidade invalido");
            txtCidade.requestFocus();
            status = false;
        }else if (cbxEstado.getSelectedItem().toString().length()==1) {
            JOptionPane.showMessageDialog(null, "Erro: Selecione um estado");
            cbxEstado.requestFocus();
            return false;
        }else{
            int numero;
            try {
                numero = Integer.parseInt(txtNumero.getText());
            } catch (NumberFormatException e) {
                numero=0;
            }
            if (numero<=0||numero>20000) {
                JOptionPane.showMessageDialog(null, "Erro: Numero invalido");
                txtNumero.requestFocus();
                return false;
            }
        }

        return status;
    }
    
