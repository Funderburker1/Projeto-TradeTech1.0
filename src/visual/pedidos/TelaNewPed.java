    private boolean validaConsulta() {
        if (txtCpfCnpj.getText().length() == 11 || txtCpfCnpj.getText().length() == 14) {
            return true;
        }
        JOptionPane.showMessageDialog(null, "Erro: CPF/CNPJ invalido");
        txtCpfCnpj.requestFocus();
        return false;
    }

    private boolean validaCampos() {
        if (txtTelefone.getText().length() < 8 && btnSim.isSelected()) {
            JOptionPane.showMessageDialog(null, "Erro: Telefone invalido");
            txtTelefone.requestFocus();
            return false;
        } else if (txtCep.getText().equals("     -   ") && btnSim.isSelected()) {
            JOptionPane.showMessageDialog(null, "Erro: Cep invalido");
            txtCep.requestFocus();
            return false;
        } else if (txtEndereco.getText().length() < 2 && btnSim.isSelected()) {
            JOptionPane.showMessageDialog(null, "Erro: Endereco invalido");
            txtEndereco.requestFocus();
            return false;
        } else if (txtBairro.getText().length() < 1 && btnSim.isSelected()) {
            JOptionPane.showMessageDialog(null, "Erro: Bairro invalido");
            txtBairro.requestFocus();
            return false;
        } else if (txtCidade.getText().length() < 2 && btnSim.isSelected()) {
            JOptionPane.showMessageDialog(null, "Erro: Cidade invalido");
            txtCidade.requestFocus();
            return false;
        } else if (tableModel.getRowCount()<=0) {
            JOptionPane.showMessageDialog(null, "Erro: Necessario pelo menos um pedido");
            return false;
        } else {
            int numero;
            try {
                numero = Integer.parseInt(txtNumero.getText());
            } catch (NumberFormatException e) {
                numero = 0;
            }
            if ((numero <= 0 || numero > 20000) && btnSim.isSelected()) {
                JOptionPane.showMessageDialog(null, "Erro: Numero invalido");
                txtNumero.requestFocus();
                return false;
            }
        }
        return true;
    }

