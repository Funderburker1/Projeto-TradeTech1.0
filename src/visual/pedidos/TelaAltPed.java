    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(validaConsulta()){
            PedidoDao pedidoDao = new PedidoDao();
            ClienteDao clienteDao = new ClienteDao();
            PedidoTemProdutoDao pedidoTemProdutoDao = new PedidoTemProdutoDao();
            ProdutoDao produtoDao = new ProdutoDao();
            
            pedidoAlterado = pedidoDao.consulta(txtPedido.getText());
            if(pedidoAlterado!=null){
                Cliente cliente = clienteDao.consultaCodigo(pedidoAlterado.getCodigoCliente());
                txtNome.setText(cliente.getNome());
                
                List<PedidoTemProduto> listaItensPedido = pedidoTemProdutoDao.consultaPedido(pedidoAlterado.getCodigo());
                txtTelefone.setText(pedidoAlterado.getTelefone());
                txtBairro.setText(pedidoAlterado.getBairro());
                txtCep.setText(pedidoAlterado.getCep());
                txtCidade.setText(pedidoAlterado.getCidade());
                txtEndereco.setText(pedidoAlterado.getLogradouro());
                txtNumero.setText(String.valueOf(pedidoAlterado.getNumero()));
                setTxtValorTotal(pedidoAlterado.getValor());
                
                for (int i = 0; i < listaItensPedido.size(); i++) {
                    String codigoProduto = String.valueOf(listaItensPedido.get(i).getCodigoProduto());
                    Produto produto = produtoDao.consulta(codigoProduto);
                    
                    listaItensPedido.get(i).setProduto(produto.getNome());
                    listaItensPedido.get(i).setQuantidadeEstoque(produto.getQuantidade());
                }
                
                listaProdutosPedidoNaoAlterado = listaItensPedido;
                tableModel.limpaTabela();
                tableModel.adicionaVariasLinhas(listaItensPedido);
                setTxtValorTotal(tableModel.getValorTotal());
                codigoPedido = pedidoAlterado.getCodigo();
                btnConfirmar.setEnabled(true);
            }else{
                JOptionPane.showMessageDialog(null, "Erro: Pedido n√£o encontrado");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        PedidoDao pedidoDao = new PedidoDao();
        PedidoTemProdutoDao pedidoTemProdutoDao = new PedidoTemProdutoDao();
        
        if(tableModel.getRowCount()>0){
            pedidoAlterado.setValor(valorTotal);
            if(pedidoDao.updade(pedidoAlterado)==1){
                System.out.println('1');
            }
            pedidoTemProdutoDao.excluir(pedidoAlterado.getCodigo());
            if(pedidoTemProdutoDao.salvar(tableModel.getDados())==1){
                System.out.println('2');
            }
            alterarEstoque(tableModel.getDados());
            btnConfirmar.setEnabled(false);
            dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Erro: Pedido sem produtos");
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

